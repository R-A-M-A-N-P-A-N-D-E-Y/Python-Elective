# Arithmetic Operator
'''

Addition [+]
Subtraction [-]
Multiplication [*]
Division [/]
Modulus [%]
Exponentiation [**]
Floor Division [//]

'''

val1 =2
val2 = 3

res = val1 + val2
print(res)

res = val1 - val2
print(res)

res = val1 * val2
print(res)

res = val1 / val2
print(res)

# Modulus Operator
res = val1 % val2
print(res)

# Exponentiation Operator
# raise the first operand to power of second
res = val1 ** val2
print(res)

# Floor Division
res = val1 // val2
print(res) # 0 not 0.56

